// Based on Random Talking Bush's QuickBMS TEX to DDS converter

namespace MithrilToolbox.Formats.Texture;

public class TexFile
{
    private readonly struct Header
    {
        public const int Magic = 0x74657800;
        public const int Unknown0 = 0x02000000;
        public const int Unknown1 = 0x0E000000;
        public const int Unknown2 = 0x02000000;
        public const byte Padding0Size = 24;
        public const int Unknown3 = 0x01000000;
        public const byte Padding1Size = 84;
    }

    private ushort Width;
    private ushort Height;
    private byte[] Flags = new byte[4];
    private byte[] ImageData = [];

    private TexFile() { }

    public TexFile(string filePath)
    {
        Read(filePath);
    }

    private void Read(string inputPath)
    {
        using FileStream stream = new(inputPath, FileMode.Open, FileAccess.Read);
        using BinaryReader reader = new(stream);

        reader.BaseStream.Seek(0x80, SeekOrigin.Begin);

        Width = reader.ReadUInt16();
        Height = reader.ReadUInt16();
        Flags = [reader.ReadByte(), reader.ReadByte(), reader.ReadByte(), reader.ReadByte()];

        reader.BaseStream.Seek(4, SeekOrigin.Current);

        int dataSize = (int)(stream.Length - 0xC);

        ImageData = reader.ReadBytes(dataSize);
    }

    public static void Write(TexFile originalTexture, TexFile newTexture, string outputPath)
    {
        using FileStream stream = new(outputPath, FileMode.Create, FileAccess.Write);
        using BinaryWriter writer = new(stream);

        writer.Write(Header.Magic);
        writer.Write(Header.Unknown0);
        writer.Write(Header.Unknown1);
        writer.Write(Header.Unknown2);
        writer.Write(new byte[Header.Padding0Size]);
        writer.Write(Header.Unknown3);
        writer.Write(new byte[Header.Padding1Size]);

        writer.Write(newTexture.Width);
        writer.Write(newTexture.Height);
        writer.Write(originalTexture.Flags);
        writer.Write(0);

        writer.Write(newTexture.ImageData);
    }

    public static void Export(TexFile texture, string outputPath)
    {
        byte[] header = DdsBaseHeader;

        if (texture.Flags[3] == 0x11 && texture.Flags[2] == 0x00)
        {
            header = DdsA8R8G8B8Header;
        }
        else if (texture.Flags[3] == 0x11 && texture.Flags[2] == 0x10)
        {
            header = DdsA8R8G8B8Header;
        }
        else if (texture.Flags[3] == 0x11 && texture.Flags[2] == 0x30)
        {
            header = DdsR8Header;
        }
        else if (texture.Flags[3] != 0x11 && texture.Flags[2] == 0x10)
        {
            header[0x57] = 0x33;
        }
        else if (texture.Flags[1] != 0x11 && texture.Flags[2] == 0x20)
        {
            header[0x57] = 0x35;
        }
        else if (texture.Flags[1] != 0x11 && texture.Flags[2] == 0x40)
        {
            header = DdsDX10Header;
        }

        if (texture.Flags[0] == 0x64)
        {
            header[0x0A] = 0x0A;
            header[0x1C] = 0x09;
            header[0x6C] = 0x08;
            header[0x6E] = 0x40;
            header[0x71] = 0xFE;
        }

        outputPath = Path.ChangeExtension(outputPath, ".dds");

        if (File.Exists(outputPath))
        {
            File.Delete(outputPath);
        }

        using FileStream stream = new(outputPath, FileMode.Create, FileAccess.Write);
        using BinaryWriter writer = new(stream);

        writer.Write(header);
        writer.Write(texture.ImageData);

        writer.BaseStream.Position = 0x0C;
        writer.Write(texture.Height);
        
        writer.BaseStream.Position = 0x10;
        writer.Write(texture.Width);
        
        writer.BaseStream.Position = 0x14;
        writer.Write(texture.ImageData.Length);
    }

    public static TexFile Import(string inputPath)
    {
        TexFile texture = new();

        using FileStream stream = new(inputPath, FileMode.Open, FileAccess.Read);
        using BinaryReader reader = new(stream);

        reader.BaseStream.Position = 0x0C;
        texture.Height = reader.ReadUInt16();
        
        reader.BaseStream.Position = 0x10;
        texture.Width = reader.ReadUInt16();

        reader.BaseStream.Position = 0x54;
        uint fourCC = reader.ReadUInt32();

        bool hasDX10Header = (fourCC == 0x30315844);
        long dataOffset = hasDX10Header ? 0x80 + 20 : 0x80;

        reader.BaseStream.Position = dataOffset;
        int dataSize = (int)(stream.Length - dataOffset);
        texture.ImageData = reader.ReadBytes(dataSize);

        return texture;
    }
    
